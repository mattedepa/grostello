package com.cap.formObject;

public class IdContatto {
	
	private int idContatto;
	private String nuovoNome;
	private String nuovoValore;
	

	public int getIdContatto() {
		return idContatto;
	}

	public void setIdContatto(int idContatto) {
		this.idContatto = idContatto;
	}

	public String getNuovoNome() {
		return nuovoNome;
	}

	public void setNuovoNome(String nuovoNome) {
		this.nuovoNome = nuovoNome;
	}

	public String getNuovoValore() {
		return nuovoValore;
	}

	public void setNuovoValore(String nuovoValore) {
		this.nuovoValore = nuovoValore;
	}

}
