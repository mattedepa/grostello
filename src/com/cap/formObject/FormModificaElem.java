package com.cap.formObject;

public class FormModificaElem {
	
	private int idContatto;
	private String nomeAltro;
	private String altroOcontatto;
	private String nuovoValore;
	
	
	public int getIdContatto() {
		return idContatto;
	}
	public void setIdContatto(int idContatto) {
		this.idContatto = idContatto;
	}
	public String getNomeAltro() {
		return nomeAltro;
	}
	public void setNomeAltro(String nomeAltro) {
		this.nomeAltro = nomeAltro;
	}
	public String getAltroOcontatto() {
		return altroOcontatto;
	}
	public void setAltroOcontatto(String altroOcontatto) {
		this.altroOcontatto = altroOcontatto;
	}
	public String getNuovoValore() {
		return nuovoValore;
	}
	public void setNuovoValore(String nuovoValore) {
		this.nuovoValore = nuovoValore;
	}

}
